import Foundation

// MARK: 프로젝트의 메타데이터를 담을 구조체
struct ProjectMetadata {
    var projectName: String
    var projectGoal: String
    var projectTarget: String
    var presentationTime: Int
    var creatAt: Date
}

struct CodableProjectMetadata: Codable {
    var projectName: String
    var projectGoal: String
    var presentationTime: Int
    var createAt: String
}
